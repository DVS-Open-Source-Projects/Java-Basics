//Write a program to display multiplication, division 
//and remainder(Modulus operator)
public class MultipicationDivisionandRemainder
{
    public static void main(String[] args)
    {
        int a, b;
        double c, d, e;
        a = 24;
        b = 2;
        c = a * b;
        d = a/b;
        e = a%b;
        System.out.println("The product is " + c);
        System.out.println("The quotient is " + d);
        System.out.println("The remainder is " + e);
    }
}
