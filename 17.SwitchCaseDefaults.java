//Use Switch Case For Default
public class SwitchCaseDefaults
{
    public static void main() 
    {
        String a="Unknown";
        switch(a)
        {
            case "Hi":
            System.out.println("Hi is the text");
            break;
            case "Hello":
            System.out.println("Hello is the text");
            break;
            default:
            System.out.println("String Not Available");
        }
    }
}