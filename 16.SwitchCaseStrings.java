//Use Switch Case For Strings
public class SwitchCaseStrings
{
    public static void main() 
    {
        String a="Hi";
        switch(a)
        {
            case "Hi":
            System.out.println("Hi is the text");
            break;
            case "Hello":
            System.out.println("Hello is the text");
            break;
        }
    }
}